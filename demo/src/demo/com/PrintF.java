package demo.com;

public class PrintF {
	public static void main(String[] args) {

        boolean myBoolean = true;
        char myChar = 'a';
        int myInt = 5;
        float myFloat = 5.99f;
        
        System.out.printf("This is my boolean: %b", myBoolean);
        System.out.println();
        System.out.printf("This is my char: %c", myChar);
        System.out.println();
        System.out.printf("This is my int: %d", myInt);
        System.out.println();
        System.out.printf("This is my float: %f", myFloat);

    }


}
